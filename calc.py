# precedence rule---
#  parenthesis -- highest priority
# exponent --- priority right to left
# *,/,//,% ---- left to right
# +,- ----- left to right 

print(2+3*4)
print(2/4) # floating point division
print(4/2)
print(4//2) # division to get integer
print(2**3)
print(2**0.5)
print(round(2**0.5, 4))
print(2**3/2*6-4*(3-4/2))     
print(3%2)
print((2+3*5/2%6))
print(12.5 % 6)
print(2**3**2)
